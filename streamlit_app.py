    with col2:
        if uploaded_file is not None:
            st.write("### ğŸ¯ Ready to identify!")
            st.write("Click the button below to:")
            st.write("âœ“ Identify the landmark")
            st.write("âœ“ Get location details")
            st.write("âœ“ Generate a complete trip plan")
            
